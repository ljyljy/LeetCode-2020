from typing import List
import heapq


class Solution:
    def kClosest(self, points: List[List[int]], K: int) -> List[List[int]]:
        self.heap, rst = [], []
        for p in points:
            dist = self._getDist(p)  # å…¥å †ï¼šâ†“ åŒæ ·distanceè¿˜è¦æ¯”x å’Œ y
            heapq.heappush(self.heap, (-dist, -p[0], -p[1]))  # è®¾ç½®æ’åºä¼˜å…ˆçº§ï¼ˆå–è´Ÿï¼‰
            if len(self.heap) > K:
                heapq.heappop(self.heap)

        while len(self.heap):
            _, x, y = heapq.heappop(self.heap)  # popé¡ºåºï¼šä¼˜å…ˆçº§æ’åºçš„ã€é€†åºã€‘
            rst.append([-x, -y])  # å†æ¬¡å–è´Ÿè¿˜åŸã€‚

        return rst[::-1]  # ç”±äºpopå‡ºçš„æ˜¯ä¼˜å…ˆçº§é€†åºï¼Œæ•…éœ€ç¿»è½¬

    def _getDist(self, p: List[int]):
        return (p[0] - 0) ** 2 + (p[1] - 0) ** 2
