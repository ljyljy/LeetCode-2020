# Definition for a binary tree node.
class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


class Solution:
    def kthLargest(self, root: TreeNode, k: int) -> int:
        if not root: return
        rst, cnt, stack = [], 1, [root]
        while stack:
            i = stack.pop()
            if isinstance(i, TreeNode):
                stack.extend([i.right, i.val, i.left])
            elif isinstance(i, int):
                rst.append(i)
                cnt += 1
                # if cnt == k: return i
        return rst[-k]  # BSTçš„ä¸­åºéå†ï¼šå‡åº
